package com.locadora.to;

public class VeiculoTO {
    private int veiCodigo;
    private String veiModelo;
    private String veiMarca;
    private String veiPlaca;
    private double veiValor;

    // Getters e Setters
    public int getVeiCodigo() {
        return veiCodigo;
    }

    public void setVeiCodigo(int veiCodigo) {
        this.veiCodigo = veiCodigo;
    }

    public String getVeiModelo() {
        return veiModelo;
    }

    public void setVeiModelo(String veiModelo) {
        this.veiModelo = veiModelo;
    }

    public String getVeiMarca() {
        return veiMarca;
    }

    public void setVeiMarca(String veiMarca) {
        this.veiMarca = veiMarca;
    }

    public String getVeiPlaca() {
        return veiPlaca;
    }

    public void setVeiPlaca(String veiPlaca) {
        this.veiPlaca = veiPlaca;
    }

    public double getVeiValor() {
        return veiValor;
    }

    public void setVeiValor(double veiValor) {
        this.veiValor = veiValor;
    }
}
